#include "MenuItem.h"

// =============================================================================
//                                 Constructor
// =============================================================================
MenuItem::MenuItem()
{
    //
}

// =============================================================================
//                                  Set Font
// =============================================================================
void MenuItem::setFont(String fontName)
{
    font_.loadFromFile(fontName);
    text_.setFont(font_);
}

// =============================================================================
//                                 Set String
// =============================================================================
void MenuItem::setString(String text)
{
    text_.setString(text);
}

// =============================================================================
//                                   Select
// =============================================================================
void MenuItem::Select()
{
    selected_ = true;
    text_.setFillColor(selectedColor_);
}

// =============================================================================
//                                  DeSelect
// =============================================================================
void MenuItem::DeSelect()
{
    selected_ = false;
    text_.setFillColor(defaultColor_);
}

// =============================================================================
//                                    Draw
// =============================================================================
void MenuItem::Draw(RenderWindow& window)
{
    window.draw(text_);
}
